import Foundation

// Desafio: Criar um convertor de temperatua para Fahrenheit
// 1 - Crie uma constante que mantém qualquer temperatura em graus Celsius
// 2 - Converta para Fahrenheit multiplicando por 9, dividino por 5 e depois adicionando 32
// Imprime o resultado para o usuário, mostrando os valores em Celsius e Fahrenheit.

var celsius = 20

let fahrenheit = (celsius * 9 / 5) + 32

print(" \(fahrenheit) F)

